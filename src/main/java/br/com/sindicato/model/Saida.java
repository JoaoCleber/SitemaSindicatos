public class Saida {

	public String descricao;
	public Date dataSaida;
	public double valorSaida;

	public String getDescricao() {
		return descricao;
	}

	public Date getDataSaida() {
		return dataSaida;
	}

	public double getValorSaida() {
		return valorSaida;
	}

}
